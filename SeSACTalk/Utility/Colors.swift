import UIKit

enum Colors {
    
    enum BrandColor {
        static let green = UIColor(named: "BrandGreen")!
        static let error = UIColor(named: "BrandError")!
        static let inactive = UIColor(named: "BrandInactive")!
        static let black = UIColor(named: "BrandBlack")!
        static let gray = UIColor(named: "BrandGray")!
        static let white = UIColor(named: "BrandWhite")!
    }
    
    enum TextColor {
        static let primary = UIColor(named: "TextPrimary")!
        static let secondary = UIColor(named: "TextSecondary")!

    }
    
    enum BackgroundColor {
        static let primary = UIColor(named: "BackgroundPrimary")!
        static let secondary = UIColor(named: "BackgroundSecondary")!

    }
    
    enum ViewColor {
        static let seperator = UIColor(named: "ViewSeperator")!
        static let alpha = UIColor(named: "ViewAlpha")!

    }
    
}
